#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		nb;
	t_list	*tmp;

	nb = 0;
	tmp = lst;
	while (tmp)
	{
		nb++;
		tmp = tmp->next;
	}
	return (nb);
}
