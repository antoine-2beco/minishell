#include "../minilib.h"

int	envcmd(char **args, t_data *data)
{
	int	i;

	if (args && args[1])
		return (0);
	i = 0;
	while (data->env[i])
	{
		ft_printf("%s\n", 1, data->env[i++]);
	}
	return (1);
}
